# Python allows you to assign values to multiple variables in one line:
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

# One Value to Multiple Variables
m = n = o = "Orange"
print(m)
print(n)
print(o)

# Unpack a Collection
fruits = ["apple", "banana", "cherry"]
s, t, u = fruits
print(s)
print(t)
print(u)