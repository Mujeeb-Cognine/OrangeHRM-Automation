# Get the characters from position 2 to position 5 (not included):
b = "Hello, World!"
print(b[2:5])

# Slice From the Start
c = "Hello, World!"
print(c[:5])

# Slice To the End
d = "Hello, World!"
print(d[2:])

# Negative Indexing
"""
Get the characters:
From: "o" in "World!" (position -5)
To, but not included: "d" in "World!" (position -2):
"""
e = "Hello, World!"
print(e[-5:-2])
